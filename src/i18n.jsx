// ─── TRANSLATE FUNCTION ───────────────────────────────────────────────────────
function translate(lang, key, vars = {}) {
  const str = DICT[lang]?.[key] ?? DICT.en_CA[key] ?? key
  return Object.entries(vars).reduce(
    (s, [k, v]) => s.replaceAll(`{${k}}`, v),
    str
  )
}

// ─── CONTEXT ──────────────────────────────────────────────────────────────────
const LangContext = createContext(null)

export function LangProvider({ children }) {
  const [lang, setLangState] = useState(() => {
    const saved = localStorage.getItem('lt_lang')
    return LANGUAGES.find(l => l.id === saved) ? saved : 'en_CA'
  })

  const langObj = LANGUAGES.find(l => l.id === lang) || LANGUAGES[0]

  useEffect(() => {
    document.documentElement.dir = langObj.dir
    document.documentElement.lang = langObj.locale
  }, [langObj])

  function setLang(id) {
    localStorage.setItem('lt_lang', id)
    setLangState(id)
  }

  const value = {
    lang,
    setLang,
    locale: langObj.locale,
    dir: langObj.dir,
    LANGUAGES,
    t: (key, vars) => translate(lang, key, vars),
  }

  return <LangContext.Provider value={value}>{children}</LangContext.Provider>
}

export function useT() {
  const ctx = useContext(LangContext)
  if (!ctx) throw new Error('useT must be used within LangProvider')
  return ctx
}
